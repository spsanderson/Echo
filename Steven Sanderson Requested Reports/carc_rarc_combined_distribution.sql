WITH CTE AS (
    SELECT DISTINCT PATIENT_CONTROL_NUMBER,
        Ins_CD,
        CLAIM_STATUS,
        BILL_TYPE,
        LINE_ADJUSTMENT_GROUP_CODE,
        LINE_ADJUSTMENT_REASON_CODE
    FROM SMS.DBO.c_remit_JOIN_ALL_TBL
    WHERE CHECK_EFT_DATE >= '2026-01-01'
    AND CHECK_EFT_DATE < '2026-07-01'
),

PR AS (
    SELECT CTE.Patient_Control_Number,
        CTE.Ins_CD,
        CTE.CLAIM_STATUS,
        CTE.BILL_TYPE,
        CTE.LINE_ADJUSTMENT_GROUP_CODE,
        CTE.LINE_ADJUSTMENT_REASON_CODE,
        [PR_INDICATOR] = CASE 
            WHEN CTE.LINE_ADJUSTMENT_GROUP_CODE = 'PR' 
            AND CTE.LINE_ADJUSTMENT_REASON_CODE NOT IN ('1', '2', '3')
                THEN 1
            ELSE 0
        END
    FROM CTE AS CTE
)

