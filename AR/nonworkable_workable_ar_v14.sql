SELECT WORKABLE_INDICATOR,
	DEPARTMENT_CLEAN,
	[TOTAL_VOLUME] = COUNT(DISTINCT (PT_NO)),
	[LOT_3] = SUM(LOT_3),
	[LOT_4] = SUM(LOT_4),
	[LOT_3_BALANACE] = SUM(LOT_3_BALANCE),
	[LOT_4_BALANCE] = SUM(LOT_4_BALANCE),
	[TOTAL_BALANCE] = SUM(BALANCE),
	[GT_$5K] = SUM(GT_5K),
	[%AR$>120] = CASE 
		WHEN SUM(BALANCE) = 0
			THEN 0
		ELSE SUM([DOLLARS>120_DAYS]) * 1.0 / SUM(BALANCE)
		END,
	AVG_WL_CYCLES = AVG(CAST(CYCLE_OF_WL AS DECIMAL(10,2))),
	[AVG_DAYS_SINCE_DSCH] = AVG(DATEDIFF(DAY, DSCH_DATE, GETDATE()))
FROM #FINAL2
--WHERE [FILE] != 'BAD DEBT'
GROUP BY WORKABLE_INDICATOR,
	DEPARTMENT_CLEAN
ORDER BY WORKABLE_INDICATOR

