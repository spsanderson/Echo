SELECT *,
	[LOT_3] = CASE WHEN AGE_BUCKET IN ('5_121-150','6_151-180','7_181-210','8_211-240','9_241-365','90_1yr-1.5yrs','91_1.5-2yrs','92_2+yrs')
					AND LEFT(INS1_CD, 1) IN ('G', 'J', 'P', 'M', 'C') AND ACCT_TYPE = 'OP' AND INS1_BALANCE > 0 THEN 1 ELSE 0 END,
	[LOT_4] = CASE WHEN LEFT(INS1_CD, 1) IN ('U', 'Y', 'O') AND ACCT_TYPE = 'OP' AND INS1_BALANCE != 0  THEN 1 ELSE 0 END,
	[LOT_3_BALANCE] = CASE WHEN AGE_BUCKET IN ('5_121-150','6_151-180','7_181-210','8_211-240','9_241-365','90_1yr-1.5yrs','91_1.5-2yrs','92_2+yrs')
					AND LEFT(INS1_CD, 1) IN ('G', 'J', 'P', 'M', 'C') AND ACCT_TYPE = 'OP' AND INS1_BALANCE > 0 THEN BALANCE ELSE 0 END,
	[LOT_4_BALANCE] = CASE WHEN LEFT(INS1_CD, 1) IN ('U', 'Y', 'O') AND ACCT_TYPE = 'OP' AND INS1_BALANCE != 0  THEN BALANCE ELSE 0 END
INTO #FINAL2
