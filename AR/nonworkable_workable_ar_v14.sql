			THEN 'UNITIZED FOLLOW UP'
		-- SPS CHANGED 2026-06-22 TO COMMENT OUT PER MEETING
		--WHEN DEPARTMENT_UPDATED IS NULL
		--	AND WORKLISTED = 'NO'
		--	AND PT_REPRESENTATIVE IN ('532','782','572','592','722','502')
		--		THEN 'VENDOR MANAGEMENT'
		-- SPS CHANGED 2026-06-22 TO REMOVE 722 PER MEETING
		WHEN DEPARTMENT_UPDATED IS NULL
			AND DEPARTMENT = 'BAD DEBT VENDORS'
			AND PT_REPRESENTATIVE IN ('721','744','746')
				THEN 'NYAG'
		-- SPS CHANGED 2026-06-22 TO REMOVE 782 PER MEETING
		WHEN DEPARTMENT_UPDATED IS NULL
			AND DEPARTMENT = 'BAD DEBT VENDORS'
			AND PT_REPRESENTATIVE IN ('780','781','783')--'782','783')
				THEN 'KOPP'
		WHEN DEPARTMENT_UPDATED IS NULL
			AND BUCKET_INS_INDICATOR = 'PT BALANCE'
			AND PT_REPRESENTATIVE IN ('000', '0', '   ')
				THEN 'PATIENT BALANCE'
		WHEN COALESCE(DEPARTMENT_UPDATED, INITIAL_DEPARTMENT) IS NULL
			THEN UPPER(LTRIM(RTRIM(REPLACE(REPLACE(DEPARTMENT, 'PENDING',''),'.',''))))
		ELSE UPPER(LTRIM(RTRIM(REPLACE(REPLACE(DEPARTMENT_UPDATED, 'PENDING',''),'.',''))))
		END,
	[RESPONSIBLE COLLECTOR],
	SUPERVISOR,
	[WORKABLE_INDICATOR] = CASE
		-- RECENTLY BILLED
		WHEN DEPARTMENT_UPDATED IN (
			'RECENTLY BILLED NON-GOVERNMENTAL PENDING',
			'RECENTLY BILLED NON-GOVERNMENTAL UNITIZED FOLLOW UP PENDING',
			'RECENTLY BILLED GOVERNMENTAL PENDING',
			'RECENTLY BILLED SPECIAL BILLING',
			'GOVERNMENTAL RECENTLY QUALIFIED SECONDARY BALANCES',
			'NON-GOVERNMENTAL RECENTLY QUALIFIED SECONDARY BALANCES',
			'SELF PAY DELETED FROM SCRUBBER',
			'SELF PAY SCRUBBER'
		)
			THEN 'RECENTLY_BILLED'
		-- WORKABLE := WORKLISTED = YES OR DEPARTMENT_UPDATED LIKE '%SCUBBER%'
		WHEN (
            WORKLISTED = 'YES'
            OR DEPARTMENT_UPDATED LIKE '%SCRUBBER%'
            --OR ACTIVE_JOC_IND = 'Y'
            OR IFPJOC = 'Y'
        )
			THEN 'WORKABLE'
		-- PENDING WORK := NOT WORKLISTED BUT HAS A VENDOR REP INCLUDING THE CLOSED ONES
		WHEN WORKLISTED = 'NO'
		AND (
			(
                PT_REPRESENTATIVE NOT IN (
				    '500','501','502',
				    '503','504','505','506',
				    '507','530','531','532','550',
				    '570','571','572','573','580',
				    '581','582','583','590','591',
				    '592','593','721','744',
				    '746','780','781','722','782','783',
				    '000','0', '   '
                )
                AND DEPARTMENT_UPDATED NOT IN ('KOPP GROUP', 'JZANUS MEDICAID APPLICATIONS','MEDCO','MED-METRIX','RTR','NYAG')
		    )
            OR DEPARTMENT_UPDATED LIKE '%PENDING%'
        )
			THEN 'WORKABLE_PENDING'
		-- INHOUSE AND DNFB
		WHEN DEPARTMENT_UPDATED IN ('INHOUSE','DNFB','OP OPEN UNIT','OP UNBILLED')
			THEN 'INHOUSE_DNFB'
		-- NOT WORKABLE := [NOT WORKLISTED AND [HAS VENDOR ACTIVE REP OR BUCKET_INS_IND = PT_BALANCE]]
		WHEN (
            WORKLISTED = 'NO'
			AND (
				BUCKET_INS_INDICATOR = 'PT BALANCE'
				OR PT_REPRESENTATIVE IN (
				    '249','250','267','500','501',
				    '503','504','505',
				    '507','530','531','550',
				    '570','571','573','580',
				    '581','583','721','744',
	        		'746','780','781','783'
		    		)
			    )
        )
            OR DEPARTMENT_UPDATED IN ('KOPP GROUP', 'JZANUS MEDICAID APPLICATIONS','MEDCO','MED-METRIX','RTR','NYAG')
			THEN 'NOT_WORKABLE'
		-- NEEDS ANALYST REVIEW
		ELSE 'NEEDS_ANALYST_REVIEW'
	END,
		EXPECTED_PAYMENT,
		CYCLE_OF_WL = TRY_CONVERT(INT, WL_CYCLES.[Cycle of WL]),
		[CYCLE_OF_WL_20+] = CASE 
			WHEN TRY_CONVERT(INT, WL_CYCLES.[Cycle of WL]) >= 20 THEN 1 
			ELSE 0 
		END
INTO #FINAL
FROM #DEPARTMENT_UPDATE_TBL AS A
LEFT JOIN (
	SELECT DISTINCT PT_NO,
		UNIT_NO,
		UNIT_DATE,
		[Cycle of WL],
		[WL status]
	FROM SMS.DBO.Pt_Accounting_Reporting_Alt_WL_Cycles
) AS WL_CYCLES ON A.Pt_No = WL_CYCLES.Pt_No
	AND ISNULL(A.UNIT_DATE, '') = ISNULL(WL_CYCLES.Unit_Date, '')
	AND ISNULL(A.UNIT_NO,'') = ISNULL(WL_CYCLES.UNIT_NO,'');


