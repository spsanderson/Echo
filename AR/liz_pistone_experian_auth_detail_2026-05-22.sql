-- Lookup accounts that have a Procedure in ProcedureInformation
DROP TABLE IF EXISTS #PC;
CREATE TABLE #PC (
    MRN VARCHAR(50),
    LOOKUP_PT_NO VARCHAR(50),
    ACTIVE_ARCHIVE_IND VARCHAR(20),
    PROC_CD VARCHAR(50),
    PROC_TYPE_CD VARCHAR(50),
    PROC_PRIO_CD VARCHAR(50),
    MODF_1 VARCHAR(50),
    MODF_2 VARCHAR(50),
    MODF_3 VARCHAR(50),
    PROC_DATE DATE,
    RESP_PTY_CD VARCHAR(50)
);
WITH PROC_CTE AS (
    SELECT [MRN] = PT.MRN,
        [LOOKUP_PT_NO] = PT.PT_NO,
        [ACTIVE_ARCHIVE_IND] = 'ACTIVE',
        [PROC_CD] = PI.[PA-PROC3-CD],
        [PROC_TYPE_CD] = PI.[PA-PROC3-CD-TYPE],
        [PROC_PRIO_CD] = PI.[PA-PROC3-PRTY],
        [MODF_1] = PI.[PA-PROC3-CD-MODF(1)],
        [MODF_2] = PI.[PA-PROC3-CD-MODF(2)],
        [MODF_3] = PI.[PA-PROC3-CD-MODF(3)],
        [PROC_DATE] = CAST(PI.[PA-PROC3-DATE] AS DATE),
        [RESP_PTY_CD] = PI.[PA-PROC3-RESP-PARTY]
    FROM #PT_LOOKUP_TBL AS PT
    INNER JOIN [ECHOLOADERDBP.UHMC.SBUH.STONYBROOK.EDU].ECHO_ACTIVE.DBO.PROCEDUREINFORMATION AS PI
        ON PT.PT_NO = CAST(PI.[PA-PT-NO-WOSCD] AS VARCHAR) + CAST(PI.[PA-PT-NO-SCD-1] AS VARCHAR)

    UNION

    SELECT [MRN] = PT.MRN,
        [LOOKUP_PT_NO] = PT.PT_NO,
