-- Join it all together
SELECT PT.MRN,
    PT.PT_NO,
    [SOFT_CODE_FOUND_IND] = CASE WHEN PC.LOOKUP_PT_NO IS NOT NULL THEN 'Y' ELSE 'N' END,
    PT_IN_INVISION_IND = CASE WHEN ALT.PT_NO IS NOT NULL THEN 'Y' ELSE 'N' END,
    [ACTIVE_ARCHIVE_IND] = ALT.ACTIVE_ARCHIVE,
    [BOTH_ACTIVE_ARCHIVE] = ISNULL(BAA.BOTH_ACTIVE_ARCHIVE, 0),
    PC.PROC_CD,
    PC.PROC_TYPE_CD,
    PC.PROC_PRIO_CD,
    PC.MODF_1,
    PC.MODF_2,
    PC.MODF_3,
    PC.PROC_DATE,
    PC.RESP_PTY_CD,
    [TREATMENT_AUTH] = ISNULL(PC_AUTH.TREATMENT_AUTH, ''),
    [AUTH_FOUND_IND] = CASE WHEN PC_AUTH.LOOKUP_PT_NO IS NOT NULL THEN 'Y' ELSE 'N' END,
    [HOSP_SVC] = ISNULL(ALT.Hosp_Svc, ''),
    [HOSP_SVC_DESCRIPTION] = ISNULL(ALT.Hosp_Svc_Description, ''),
    [DISTINCT_PTNO_IND] = CASE WHEN ROW_NUMBER() OVER(PARTITION BY PT.PT_NO ORDER BY PT.PT_NO, PC.PROC_PRIO_CD) = 1 THEN 'Y' ELSE 'N' END,
    [IP_OP_IND] = ALT.ACCT_TYPE,
    ALT.*
FROM #PT_LOOKUP_TBL AS PT
LEFT JOIN #PC AS PC
    ON PT.PT_NO = PC.LOOKUP_PT_NO
LEFT JOIN #PC_AUTH AS PC_AUTH
    ON PT.PT_NO = PC_AUTH.LOOKUP_PT_NO
LEFT JOIN SMS.dbo.Pt_Accounting_Reporting_ALT AS ALT
    ON Pt.PT_NO = ALT.PT_NO
LEFT JOIN (
    SELECT ZZZ.LOOKUP_PT_NO,
        [BOTH_ACTIVE_ARCHIVE] = 1 
    FROM #PC AS ZZZ
    WHERE ZZZ.ACTIVE_ARCHIVE_IND IN ('ACTIVE', 'ARCHIVED')
    GROUP BY ZZZ.LOOKUP_PT_NO
    HAVING COUNT(DISTINCT ZZZ.ACTIVE_ARCHIVE_IND) = 2
) AS BAA
    ON PT.PT_NO = BAA.LOOKUP_PT_NO
